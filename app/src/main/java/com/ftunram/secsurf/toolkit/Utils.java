package com.ftunram.secsurf.toolkit;

import java.io.File;

/**
 *
 * @author I
 */
public class Utils {

    public final static String jpeg = "jpeg";
    public final static String jpg = "jpg";
    public final static String gif = "gif";
    public final static String tiff = "tiff";
    public final static String tif = "tif";
    public final static String png = "png";
    public final static String mp4="mp4";
    public final static String avi="avi";
    public final static String flv="flv";
    public final static String mpg="mpg";
    public final static String mov="mov";
    public final static String enc="enc";

    /*
     * Get the extension of a file.
     */  
    public static String getExtension(File f) {
        String ext = null;
        String s = f.getName();
        int i = s.lastIndexOf('.');

        if (i > 0 &&  i < s.length() - 1) {
            ext = s.substring(i+1).toLowerCase();
        }
        return ext;
    }
}

